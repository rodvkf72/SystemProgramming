#include "sum_lib.h"

float sum(float first, float second){
float s = (first + second);
return s;
}
float sub(float first, float second){
float su = (first - second);
return su;
}
float mul(float first, float second){
float m = (first * second);
return m;
}
float div(float first, float second){
float d = (first / second);
return d;
}
